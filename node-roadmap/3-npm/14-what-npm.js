


//! QUÉ ES NPM
// npm (Node Package Manager) es el gestor de paquetes de Node.js. Se utiliza para instalar,
// administrar y compartir paquetes (bibliotecas, herramientas, frameworks) que facilitan el
// desarrollo en Node.js.


//* Cuando instalas Node.js, npm ya viene incluido por defecto.



//! Características principales de npm
// - Gestión de dependencias: Instala, actualiza y elimina paquetes en tu proyecto.
// - Scripts: Permite definir y ejecutar comandos personalizados dentro de un proyecto.
// - Registro de paquetes: Puedes publicar y compartir tus propios paquetes con la comunidad.
// - Versionado: Controla las versiones de los paquetes instalados para evitar conflictos.
// - Gestión de proyectos monorepo: Permite manejar múltiples paquetes dentro de un solo repositorio con npm workspaces.
// - Compatible con npx: Ejecuta paquetes sin necesidad de instalarlos globalmente.



//! Ejemplo básico de uso de npm
//* 1. Inicializar un proyecto con npm

// npm init -y

// Esto crea un archivo package.json con la configuración del proyecto.

//* 2. Instalar un paquete (ejemplo: Express)

// npm install express

// Esto agrega express a node_modules y lo registra en package.json.

//* 3. Instalar un paquete de manera global

// npm install -g nodemon

// Esto instala nodemon en el sistema para que esté disponible en cualquier proyecto.

//* 4. Ejecutar un script definido en package.json

// npm run start

// Si package.json tiene un script "start": "node app.js", esto ejecutará app.js.




//! Casos de uso de npm
// - Crear un proyecto Node.js con todas sus dependencias organizadas.
// - Instalar frameworks y bibliotecas como Express, React, Jest, etc.
// - Automatizar tareas con scripts personalizados (ejemplo: compilar código, ejecutar tests, iniciar un servidor, etc.).
// - Publicar y compartir paquetes reutilizables con la comunidad.

