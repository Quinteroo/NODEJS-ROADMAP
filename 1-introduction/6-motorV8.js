



//! EL MOTOR V8 DE GOOGLE EN NODE.JS
// - V8 es el motor de JavaScript desarrollado por Google que //* ejecuta el código JS en Google Chrome y en Node.js (desde 2009).
// - Es independiente del navegador, lo que permitió la creación de Node.js
// y otras tecnologías como Electron.
// - Todos los motores siguen el //* estándar ECMAScript (ES-262).
//* - V8 está escrito en C++, es portable y optimizado para rendimiento.



//! Existen otros motores de JS:
// > SpiderMonkey (Firefox)
// > JavaScriptCore/Nitro (Safari)
// > V8 también en Edge (basado en Chromium)



//! Compilación Just-In-Time (JIT):
// > Aunque JavaScript es interpretado, los motores modernos lo compilan para acelerar la ejecución.
// > Desde 2009, los navegadores han implementado compiladores JIT para mejorar la eficiencia.
// > Esto es clave en aplicaciones web modernas que ejecutan grandes volúmenes de código por largos períodos.


// En resumen, //* V8 mejora el rendimiento de JavaScript al compilarlo y optimizarlo en tiempo real,
