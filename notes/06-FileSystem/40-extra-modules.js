

//! OTROS MÓDULOS INTERESANTES
// Existen varias librerías en Node.js para trabajar con diferentes tipos de documento* s. Aquí te dejo algunas populares:

// * 1. PDFs:
//   - pdf-parse
//    - pdf-lib: Permite crear, modificar y firmar PDFs.
//    - pdfkit: Utilizado para generar PDFs desde cero, incluye funcionalidades de escritura, gráficos y texto.

// * 2. Word (DOCX):
//    - mammoth: Convierte archivos DOCX a HTML o texto.
//    - docxtemplater: Permite crear plantillas y reemplazar los valores dinámicos dentro de un archivo DOCX.

// * 3. Excel (XLSX):
//    - xlsx: Para leer, escribir y manipular archivos Excel en formato XLSX.
//    - exceljs: Permite trabajar con archivos XLSX, creando, modificando y exportando hojas de cálculo.

// * 4. PowerPoint (PPTX):
//    - pptxgenjs: Librería para generar presentaciones PowerPoint de manera programática.

// * 5. Text (TXT):
//    - text-encoding: Una librería para manipular la codificación de textos, muy útil cuando trabajas con archivos `.txt`.
   
// * 6. CSV:
//    - csv-parser: Lee archivos CSV y los convierte en objetos de JavaScript.
//    - papaparse: Librería robusta para analizar y convertir archivos CSV a objetos.

// * 7. HTML:
//    - cheerio: Una librería para trabajar con archivos HTML en el servidor, similar a jQuery, permite manipular el DOM y hacer scraping.

// * 8. Images:
//    - sharp: Librería para el procesamiento de imágenes (redimensionar, convertir formatos, etc.).
//    - jimp: Otra librería para la manipulación de imágenes, similar a Sharp, pero con una API más sencilla.

// Cada una de estas librerías está diseñada para trabajar con tipos específicos de documentos, permitiendo 
// manipular, extraer o generar archivos según el formato.