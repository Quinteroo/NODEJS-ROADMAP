


// //! INTRO JAVASCRIPT / TYPESCRIPT
//? JavaScript es un lenguaje de alto nivel, interpretado, multiparadigma y multiplataforma, que
//? originalmente se diseñó para el navegador, pero hoy también se usa en el backend (gracias a Node.js).



//* Alto nivel:
// Significa que el lenguaje está más cerca del lenguaje humano que del lenguaje máquina. No
// necesitas preocuparte por cosas como la gestión manual de memoria, punteros, registros, etc.
// Está pensado para que el desarrollador escriba código de forma sencilla y rápida.

// ---

//* Interpretado:
// Un lenguaje interpretado no necesita ser compilado previamente a código máquina como lo harías
// con C o C++. En vez de eso, un intérprete (como el motor de JavaScript del navegador o Node.js)
// lee y ejecuta el código línea por línea.

// - Aunque con herramientas modernas como V8 (el motor de Chrome y Node), se hace una especie de
// compilación "justo a tiempo" (JIT), sigue considerándose un lenguaje interpretado.

// ---

//* Multiparadigma:
// JavaScript admite varios estilos de programación. Puedes escribir:

// - Programación imperativa: con estructuras clásicas (if, for, etc.)
// - Programación orientada a objetos: con clases y objetos
// - Programación funcional: con funciones puras, map, reduce, etc.

// Esto lo hace muy flexible.

// ---

//* Multiplataforma:
// JavaScript puede ejecutarse en:

// - Navegadores (Chrome, Firefox, Safari, etc.)
// - Servidores (Node.js)
// - Apps móviles (React Native)
// - Apps de escritorio (Electron)
// - Dispositivos IoT, etc.


//! TypeScript:
// Es un superset de JavaScript creado por Microsoft que añade tipado estático y otras funcionalidades
// de desarrollo profesional, como interfaces, tipos personalizados, decoradores, etc. Antes de
// ejecutarlo, se transpila a JavaScript.


//! Ventajas de usar TypeScript:
// - Detectas errores antes de ejecutar el código
// - Mejor autocompletado y documentación en el editor
// - Más fácil de mantener en proyectos grandes


